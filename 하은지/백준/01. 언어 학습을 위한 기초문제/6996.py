# 참고 및 성공 코드 


# [0] 변수 선언부 
T = int(input())


# [1] 애너그램 확인 후 출력 
for i in range(T):
    word_1, word_2 = map(str, input().split())
    w1 = list(word_1)
    w2 = list(word_2)
    
    if(sorted(w1) == sorted(w2)):
        print(word_1 + " & " + word_2, "are anagrams.")
        
    else:
        print(word_1 + " & " + word_2, "are NOT anagrams.")
